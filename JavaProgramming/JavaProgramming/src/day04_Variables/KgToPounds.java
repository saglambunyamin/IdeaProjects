package day04_Variables;

public class KgToPounds {

    public static void main(String[] args) {

        double kg = 10.8;
        double pounds = 2.20;

        double KgToPounds = kg * pounds;

        System.out.println("kg = " + kg);
        System.out.println("KgToPounds = " + KgToPounds);

    }

}
